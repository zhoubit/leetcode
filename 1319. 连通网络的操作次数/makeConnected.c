/*
1319. 连通网络的操作次数
用以太网线缆将 n 台计算机连接成一个网络，计算机的编号从 0 到 n-1。线缆用 connections 表示，
其中 connections[i] = [a, b] 连接了计算机 a 和 b。
网络中的任何一台计算机都可以通过网络直接或者间接访问同一个网络中其他任意一台计算机。
给你这个计算机网络的初始布线 connections，你可以拔开任意两台直连计算机之间的线缆，并用它连接一对未直连的计算机。
请你计算并返回使所有计算机都连通所需的最少操作次数。如果不可能，则返回 -1 。 

示例 1：
输入：n = 4, connections = [[0,1],[0,2],[1,2]]
输出：1
解释：拔下计算机 1 和 2 之间的线缆，并将它插到计算机 1 和 3 上。

示例 2：
输入：n = 6, connections = [[0,1],[0,2],[0,3],[1,2],[1,3]]
输出：2

示例 3：
输入：n = 6, connections = [[0,1],[0,2],[0,3],[1,2]]
输出：-1
解释：线缆数量不足。

示例 4：
输入：n = 5, connections = [[0,1],[0,2],[3,4],[2,3]]
输出：0

提示：
1 <= n <= 10^5
1 <= connections.length <= min(n*(n-1)/2, 10^5)
connections[i].length == 2
0 <= connections[i][0], connections[i][1] < n
connections[i][0] != connections[i][1]
没有重复的连接。
两台计算机不会通过多条线缆连接。

解题历程：
1. 应该是跟集合相关的解法
2. 如果两台电脑都没在某个集合里的话，那么就将1连接到0
3. 如果其中一台没在某个集合里的话，那么就把它连到集合中
4. 如果两台电脑都有集合，那么就将这条网线统计到lines中
5. 最后统计集合的数量 internet，对比Internet和lines的大小，返回结果
*/

int makeConnected(int n, int** connections, int connectionsSize, int* connectionsColSize)
{
	int lines = 0, internet = 0, c1, c2;
	int i;
	int group[n], current; 
	
	memset (group, -1, sizeof(int)*n);
	
	for (i = 0; i < connectionsSize; i++)
	{
		c1 = connections[i][0];
		c2 = connections[i][1]; 
		
		if (group[c1] != -1)
		{
			if (group[c2] != -1) //两台电脑都在网路中时，就要看他们是不是在同i一个网络中
			{
				while (group[c1] >= 0)
					c1 = group[c1];
				while (group[c2] >= 0)
					c2 = group[c2];
				if (c1 != c2)
					group[c2] = c1;
				else
					lines++;
			}
			else 	//group[c2] == -1
			{
				while (group[c1] >= 0)
					c1 = group[c1];
				if (c1 != c2)
					group[c2] = c1;
				else
					lines++;
			}
		}
		else	//group[c1] == -1
		{
			if (group[c2] == -1)
				group[c2] = c1;
			else	//group[c2] != -1
			{
				while (group[c2] >= 0)
					c2 = group[c2];
				if (c1 != c2)
					group[c1] = c2;
				else
					lines++;
			}
		}
	}
	
	for (i = 0; i < n; i++)
		if (group[i] < 0)
			internet++;
	internet--;
	
	if (internet > lines)
		return -1;
	else
		return internet;
}
