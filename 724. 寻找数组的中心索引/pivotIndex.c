/*
724. 寻找数组的中心索引

给定一个整数类型的数组 nums，请编写一个能够返回数组 “中心索引” 的方法。
我们是这样定义数组 中心索引 的：数组中心索引的左侧所有元素相加的和等于右侧所有元素相加的和。
如果数组不存在中心索引，那么我们应该返回 -1。如果数组有多个中心索引，那么我们应该返回最靠近左边的那一个。
 
示例 1：
输入：
nums = [1, 7, 3, 6, 5, 6]
输出：3
解释：
索引 3 (nums[3] = 6) 的左侧数之和 (1 + 7 + 3 = 11)，与右侧数之和 (5 + 6 = 11) 相等。
同时, 3 也是第一个符合要求的中心索引。

示例 2：
输入：
nums = [1, 2, 3]
输出：-1
解释：
数组中不存在满足此条件的中心索引。
 
说明：
1. nums 的长度范围为 [0, 10000]。
2. 任何一个 nums[i] 将会是一个范围在 [-1000, 1000]的整数。

解题思路：
1. 很显然，使用双指针就可以解决这个问题了
2. 左右指针中，谁指向的值的和小，谁就往中间移动一个位置，如果两个值的和相等的话，那么同时往中间移动一个位置
*/

int pivotIndex(int* nums, int numsSize)
{
	if (numsSize < 3) //处理 0，1，2 的长度的数据
	{
		if (numsSize % 2 == 0)
			return -1;
		else 
			return 0;
	}
	
	int left = 0, right = numsSize-1, Lval = nums[left], Rval = nums[right];
	
	while (left + 2 < right)
	{
		if (Lval > Rval)
			Rval += nums[--right];
		else if (Lval < Rval)
			Lval += nums[++left];
		else
		{
			Rval += nums[--right];
			Lval += nums[++left];
		}
	}
	
	if (left + 2 == right && Rval == Lval)
		return right+1;
	
	return -1;
}
